export function isLanguage(value: unknown): value is Language {
  return value === "zh" || value === "en";
}

export function translate(key: string, language: Language = "en", params: Params = {}, fallback?: string): string {
  const template = messages[language][key] || messages.en[key] || fallback || key;
  return template.replace(/\{([^}]+)\}/g, (match, name) => {
    const value = params[name];
    return value === null || value === undefined ? match : `${value}`;
  });
}

export function translateLabel(value: unknown, language: Language = "en"): string {
  const raw = `${value || "unknown"}`;
  const key = raw.trim().toLowerCase().replace(/\s+/g, "_");
  return translate(`label.${key}`, language, {}, raw.replaceAll("_", " "));
}

export function provideI18n(language: Ref<Language>): I18nContext {
  const context = createContext(language);
  provide(I18N_KEY, context);
  return context;
}

export function useI18n(): I18nContext {
  return inject<I18nContext>(I18N_KEY, createContext(fallbackLanguage));
}

function createContext(language: Ref<Language>): I18nContext {
  return {
    language,
    t: (key, params, fallback) => translate(key, language.value, params, fallback),
    label: (value) => translateLabel(value, language.value),
    status: (value) => translateLabel(value, language.value)
  };
}
